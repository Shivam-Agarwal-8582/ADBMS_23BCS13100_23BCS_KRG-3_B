CREATE TABLE A 
(
    EMPID INT PRIMARY KEY,
   ENAME VARCHAR(MAX), SALARY INT
);

INSERT INTO A 
(EMPID, ENAME, SALARY)
 VALUES (1, 'AA', 1000), (2, 'BB', 300);

CREATE TABLE B 
(
    EMPID INT PRIMARY KEY,
     ENAME VARCHAR(MAX), 
     SALARY INT
);

INSERT INTO B (EMPID, ENAME, SALARY)
VALUES
(2, '88', 400), (3, 'CC', 100);

SELECT EMPID, MIN(ENAME) AS ENAME, MIN(SALARY) AS SALARY FROM

(

SELECT FROM A

UNION ALL

SELECT FROM B

AS INTERMEDIATE RESULT

GROUP BY EMPID
)